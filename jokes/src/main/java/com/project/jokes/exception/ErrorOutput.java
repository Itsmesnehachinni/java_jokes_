package com.project.jokes.exception;

public class ErrorOutput {

	private String message;
	
	public ErrorOutput() {
		super();
	}
	
	public ErrorOutput(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
	
}
